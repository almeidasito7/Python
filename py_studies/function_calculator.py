# Creating the module

# Sum function
def sum_vals(a, b):
    return float(a) + float(b)

# Subtraction function
def subtract(a, b):
    return float(a) - float(b)

# Division function
def divide(a, b):
    if b == 0:
        return 0
    return float(a) / float(b)

# Multiplication function
def multiply(a, b):
    return float(a) * float(b)
