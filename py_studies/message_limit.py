# Creation of variable with null initial value
i = 0

# While the counter variable does not reach the limit
while(i < 10):
    # For each repetition, the message below is displayed
    print("One more message, with i equal to: {}".format(i))
    i = i + 1
# After the counter variable reaches the limit, the message below is displayed
print("Last message, with i equal to: {}".format(i))
