# Out of order values
values = [1, 7, 7, 19, 3, 2, 11, 15, 6, 1, 5]
# Displaying the list
print("The list was created like this: {}".format(values))
# Counting elements
count = values.count(7)
print("In this list, the number 7 appears {} times".format(count))
# Reversing the list
values.reverse()
print("The list is now reversed: {}".format(values))
# Sorting the list
values.sort()
print("The list is now sorted: {}".format(values))
